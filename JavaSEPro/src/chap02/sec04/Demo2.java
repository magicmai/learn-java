package chap02.sec04;

public class Demo2 {
	
	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		// +运算符
		System.out.println(a+"+"+b+"="+(a+b));
		// -运算符
		System.out.println(a+"-"+b+"="+(a-b));
		// *运算符
		System.out.println(a+"*"+b+"="+(a*b));
		// /运算符，整型除以整型为整型
		System.out.println(a+"/"+b+"="+(a/b));
		// %运算符，取模
		System.out.println(a+"%"+b+"="+(a%b));
	}
}
